        loadi r2, r0
        cmp r2, r7
        jeq get_pos.achou

        cmp r1, r3
        jeq get_pos.nao_achou

        inc r0
        inc r1

        jmp get_pos.loop

    get_pos.achou:
        loadn r6, r1
        jmp get_pos.fim

    get_pos.nao_achou:
        loadn r6, #6900

    get_pos.fim:
        pop r3
        pop r2
        pop r1
        pop r0
        rts


